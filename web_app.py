def calculate_bha_cluster(foot_length_mm):
    """Proprietary Logic: Maps foot length to BHA Size Category VIII Clusters."""
    if foot_length_mm < 245:
        return "Cluster VI (Narrow)"
    elif 245 <= foot_length_mm < 260:
        return "Cluster VII (Standard)"
    elif 260 <= foot_length_mm < 275:
        return "Cluster VIII (Wide)"
    else:
        return "Cluster IX (Extra Wide)"


